// derivePackageShort
// ---------------------------------------------------------------------------

describe('derivePackageShort', () => {
  it('@capsuletech/web-ui → web-ui', () => {
    expect(derivePackageShort('@capsuletech/web-ui')).toBe('web-ui');
  });

  it('@my-org/my-lib → my-lib', () => {
    expect(derivePackageShort('@my-org/my-lib')).toBe('my-lib');
  });

  it('@scope/a-b-c → a-b-c', () => {
    expect(derivePackageShort('@scope/a-b-c')).toBe('a-b-c');
  });

  it('unscoped name stays unchanged', () => {
    expect(derivePackageShort('some-lib')).toBe('some-lib');
  });

  it('@scope without slash → unchanged (guard against malformed)', () => {
    // Edge: '@scope' with no slash — not a standard npm name but should not crash.
    expect(derivePackageShort('@scope')).toBe('@scope');
  });
});

// ---------------------------------------------------------------------------
// generateDocsSourcesRuntime
// ---------------------------------------------------------------------------

describe('generateDocsSourcesRuntime', () => {
  it('contains setDocsSources import from @capsuletech/web-docs', () => {
    const out = generateDocsSourcesRuntime([]);
    expect(out).toContain("import { setDocsSources } from '@capsuletech/web-docs'");
  });

  it('auto-generated header comment is present', () => {
    const out = generateDocsSourcesRuntime([]);
    expect(out).toContain('Auto-generated by capsule-registry');
    expect(out).toContain('Do not edit');
  });

  it('emits root entry when provided', () => {
    const out = generateDocsSourcesRuntime([{ key: 'root', pkg: '@capsuletech/web-docs' }]);
    expect(out).toContain(`"root": () => import('@capsuletech/web-docs/docs.json')`);
  });

  it('emits web-ui entry when provided', () => {
    const out = generateDocsSourcesRuntime([{ key: 'web-ui', pkg: '@capsuletech/web-ui' }]);
    expect(out).toContain(`"web-ui": () => import('@capsuletech/web-ui/docs.json')`);
  });

  it('emits multiple entries in order', () => {
    const entries = [
      { key: 'root', pkg: '@capsuletech/web-docs' },
      { key: 'web-ui', pkg: '@capsuletech/web-ui' },
    ];
    const out = generateDocsSourcesRuntime(entries);
    const rootIdx = out.indexOf('"root"');
    const webUiIdx = out.indexOf('"web-ui"');
    expect(rootIdx).toBeGreaterThanOrEqual(0);
    expect(webUiIdx).toBeGreaterThanOrEqual(0);
    expect(rootIdx).toBeLessThan(webUiIdx);
  });

  it('empty entries → setDocsSources called with empty object', () => {
    const out = generateDocsSourcesRuntime([]);
    expect(out).toContain('setDocsSources({');
    expect(out).toContain('});');
    // No import paths for packages.
    expect(out).not.toContain("import(");
  });

  it('ends with newline', () => {
    const out = generateDocsSourcesRuntime([]);
    expect(out.endsWith('\n')).toBe(true);
  });
});

