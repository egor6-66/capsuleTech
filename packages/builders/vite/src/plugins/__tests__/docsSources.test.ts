/**
 * Tests for createDocsSourcesSubGenerator + pure helpers.
 *
 * What is covered:
 *
 * ── derivePackageShort ──────────────────────────────────────────────────────
 *  - @scope/name → name (strip scope)
 *  - @capsuletech/web-ui → web-ui
 *  - @my-org/my-lib → my-lib
 *  - unscoped name → unchanged
 *
 * ── generateDocsSourcesRuntime ──────────────────────────────────────────────
 *  - Contains setDocsSources import
 *  - Contains 'root' entry when rootVault entries provided
 *  - Contains per-package entry
 *  - Multiple entries emitted correctly
 *  - Empty entries array → no entries between braces
 *
 * ── createDocsSourcesSubGenerator (SubGenerator contract) ───────────────────
 *  - id = 'docs-sources', order = 50
 *  - match() returns false (no src file events)
 *  - onEvent() returns false
 *  - onAppConfigChange() marks dirty and returns true
 *  - bootstrap() returns globals phase with correct importPath
 *
 * ── flush() behaviour ───────────────────────────────────────────────────────
 *  - No docs field in config → removeOut called (cleanup)
 *  - docs: {} (empty) → removeOut called
 *  - docs.rootVault=true, packages=[] → only root entry emitted
 *  - docs.rootVault=false, packages=['@capsuletech/web-ui'] with docsJson export → web-ui entry
 *  - Package without ./docs.json export → skip + warn (no entry)
 *  - All packages skipped → removeOut called (empty entries)
 *  - Not dirty and not forced → no write / no remove
 *  - onAppConfigChange → dirty → flush writes
 */

import { resolve } from 'node:path';
import { afterEach, describe, expect, it, vi } from 'vitest';
import {
  checkDocsJsonExport,
  createDocsSourcesSubGenerator,
  derivePackageShort,
  generateDocsSourcesRuntime,
} from '../codegen';
import type { AppConfigShape, CodegenContext } from '../codegen';

